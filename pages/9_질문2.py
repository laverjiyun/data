import streamlit as st

# 웹앱 제목 설정
st.title("🚗 우리들의 여행 플레이리스트 설문조사")
st.subheader("친구들과 함께 듣는 드라이브 송 취합용")

st.divider()  # 구분선

# ----------------------------------------
# 질문 2: 최악의 음악 매너 (다중 선택)
# ----------------------------------------

# 질문 제목
st.markdown("#### **Q2. 네가 생각하는 최악의 조수석(또는 뒷자리) 음악 매너는?** (복수 선택 가능)")

# 선택지 정의
options = [
    "🤫 나만 아는 초 마이너한 노래(또는 씹덕곡) 내내 틀기",
    "⏭️ 1절만 듣고 질린다고 계속 다음 곡으로 넘기기",
    "🗣️ 다들 조용히 가고 싶은데 혼자 쉬지 않고 마이크 잡고 떼창하기",
    "💤 플레이리스트 같이 고르자고 해놓고 차 타자마자 이어폰 끼고 자기",
    "📱 노래 소리보다 더 크게 유튜브나 쇼츠 쇼츠 소리 키워놓기",
]

# multiselect 컴포넌트 생성 (다중 선택)
bad_manners = st.multiselect(
    label="조심해줬으면 하는 비매너 행동을 모두 골라줘!",
    options=options,
    placeholder="여기를 클릭해서 비매너 행동을 골라보세요 (여러 개 가능)"
)

# ----------------------------------------
# 결과 확인용 (디버깅/테스트용 코드)
# ----------------------------------------
st.divider()

if bad_manners:
    st.warning(f"🚨 친구가 기겁하는 매너들: {', '.join(bad_manners)}")
else:
    st.info("이 정도면 평화주의자! 워스트 매너를 골라주세요.")
