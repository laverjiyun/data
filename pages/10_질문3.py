import streamlit as st

# 웹앱 제목 설정
st.title("🚗 우리들의 여행 플레이리스트 설문조사")
st.subheader("친구들과 함께 듣는 드라이브 송 취합용")

st.divider()  # 구분선

# ----------------------------------------
# 질문 3: 가사 따라 부르는 정도 (슬라이더 선택)
# ----------------------------------------

# 질문 제목
st.markdown("#### **Q3. 노래를 들을 때 '가사'를 따라 부르는 편이야?**")
st.caption("왼쪽일수록 조용한 감상파, 오른쪽일수록 열정적인 떼창파!")

# slider 컴포넌트 생성
# 1부터 4까지의 단계로 설정하고, 각 단계별 설명을 매칭합니다.
sing_level = st.slider(
    label="나의 차 안 떼창 레벨을 선택해줘",
    min_value=1,
    max_value=4,
    value=2,  # 기본 시작 위치
    step=1
)

# 선택한 숫자에 따라 친구들에게 보여줄 직관적인 텍스트 변환
level_explain = {
    1: "🤫 음소거 모드 (가사 안 보고 조용히 음악만 감상함)",
    2: "🎵 흥얼거림 모드 (멜로디나 비트만 가볍게 음음~ 따라함)",
    3: "🎤 립싱크 모드 (조용히 입모양으로 가사를 완벽히 읊조림)",
    4: "🔥 콘서트 모드 (가사 다 외움. 목 터져라 부르는 차 안의 아이유)"
}

# ----------------------------------------
# 결과 확인용 (디버깅/테스트용 코드)
# ----------------------------------------
st.divider()

st.info(f"현재 선택된 레벨: {sing_level}단계")
st.success(f"나의 성향: {level_explain[sing_level]}")
